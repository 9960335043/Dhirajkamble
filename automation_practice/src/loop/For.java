package loop;

public class For {

	public static void main(String[] args) {
		
		//1 to 10
		for(int b = 1; b<=10; b++) {
			System.out.println(b);
		}

		System.out.println("-------------------------");
		
		//10 to 1
		for(int a=10; a>=1; a--) {
			System.out.println(a);
		}
		System.out.println("---------------------");
		
		//51 to 100
		for(int c=51; c<=100; c++) {
			System.out.println(c);
		}
		System.out.println("----------------------------------");
		
		
		for(float v = 0.1f; v<=10; v++) {
			System.out.println(v);
			
		}
		//iterate
		//iteration
	}

}
