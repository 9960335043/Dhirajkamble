package decision_making;

public class String_switch {

	public static void main(String[] args) {
		String name = "dhiraj";
		
		switch (name) {
		
		case "akshay":
		System.out.println("surname - kamble");
		break;
		
		case "suraj":
		System.out.println("surname - sdff");
		break;
		
		case "pratik":
		System.out.println("surname - bjsjjd");
		break;
		
		case "dhiraj":
			System.out.println("surname - kamble");
			break;
			
		default:
			System.out.println("all the above cases are false");
		}
		}

	}


