package wrapper_1;

public class Primitive_Wrapperclass {

	public static void main(String[] args) {
		
		int a = 30;
		Integer b = Integer.valueOf(a);
		System.out.println(b);

		float z = 345.876f;
		Float n = Float.valueOf(z);
		System.out.println(n);
		
		float z1 = 345.876f;
		Float u = Float.valueOf(z1);
		System.out.println(u);
	}

}
