package decision_making;

public class String_datatype {

	public static void main(String[] args) {
		String s = "wardha";
		
		if(s=="wardha") {
			System.out.println("wardha");
			if(s=="nagpur") {
				System.out.println("nagpur");
				
			}
		}else {
			System.out.println("none of the above");
		}

	}

}
