package loop;

public class Even {

	public static void main(String[] args) {
		
		//even = divisible by 2
		//odd = not divisible by 2
		
		//print first 50 no. 
		// for even number
		for(int x = 1; x<=100; x++) {
			if(x%2==0) {
				System.out.println(x);
			}
		}
		for(int z=2; z<=100; z=z+2) {
			System.out.println(z);

	}

}}
