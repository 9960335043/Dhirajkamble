package casting;

public class Narrowing {

	public static void main(String[] args) {
		long a = 100;
		int b = (int) a;
		System.out.println(b);

		
		double s = 88.5678;
		float q = (float) s;
		System.out.println(q);
		
		double s1 = 88.5678;
		int r = (int) s1;
		System.out.println(r);
		
		
	}

}
